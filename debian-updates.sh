#! /bin/bash

##keeping the system clean

echo '#########################################################################'
echo 'Updating and Upgrading packages'
echo '#########################################################################'
sleep 2

sudo apt update && sudo apt upgrade

echo '#########################################################################'
echo 'Done' 
echo 'Removing useless packages'
echo '#########################################################################'
sleep 2

sudo apt autoremove 

echo '#########################################################################'
echo 'Done' 
echo 'Appending to history file'
echo '#########################################################################'
sleep 2

echo 'debian-updates script ran on ' $(date) >> log.txt

echo '#########################################################################'
echo 'Done' 
echo '#########################################################################'
